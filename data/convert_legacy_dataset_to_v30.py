#!/usr/bin/env python3
from __future__ import annotations

import argparse
import contextlib
import datetime as dt
import json
import os
import shutil
import sys
from dataclasses import dataclass
from pathlib import Path


SCRIPT_DIR = Path(__file__).resolve().parent
MAIN_ROOT = SCRIPT_DIR.parent
SCRIPTS_ROOT = MAIN_ROOT / "scripts"
if str(SCRIPTS_ROOT) not in sys.path:
    sys.path.insert(0, str(SCRIPTS_ROOT))

from dataset_utils import (  # type: ignore
    DEFAULT_LEROBOT_V30_COMPAT_CACHE_ROOT,
    is_legacy_lerobot_dataset_root,
    is_lerobot_dataset_root,
    resolve_dataset_root,
)


KNOWN_V30_ROOT_ENTRIES = {"data", "meta", "videos"}
DEFAULT_CONVERSION_METADATA_FILENAME = ".legacy_to_v30_conversion.json"


@dataclass(slots=True)
class DatasetScanSummary:
    total_files: int
    data_parquet_files: int
    video_files: int
    extra_files: int
    extra_entries: list[str]


def get_tqdm():
    try:
        from tqdm.auto import tqdm
    except ModuleNotFoundError:
        return None
    return tqdm


def progress_write(progress, message: str) -> None:
    if progress is not None and hasattr(progress, "write"):
        progress.write(message)
        return
    print(message)


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description=(
            "Convert a legacy LeRobot v2.1 dataset into a v3.0 dataset under "
            "`data/`, with tqdm-based progress visualization."
        )
    )
    parser.add_argument(
        "dataset",
        type=str,
        nargs="?",
        help=(
            "Legacy dataset root path, a path inside `data/`, or a dataset id "
            "relative to `data/`."
        ),
    )
    parser.add_argument(
        "--dataset",
        dest="dataset_option",
        type=str,
        default=None,
        help="Alias of the positional dataset argument.",
    )
    parser.add_argument(
        "--output-dir",
        type=str,
        default=None,
        help=(
            "Output dataset directory. Defaults to a sibling directory named "
            "`<dataset_name>_v30` under `data/`."
        ),
    )
    parser.add_argument(
        "--overwrite-output",
        action="store_true",
        help="Replace an existing output directory.",
    )
    extra_group = parser.add_mutually_exclusive_group()
    extra_group.add_argument(
        "--copy-extra-artifacts",
        dest="copy_extra_artifacts",
        action="store_true",
        help=(
            "Copy extra top-level artifacts not generated by the official converter, "
            "such as `.signature_cache` or `normalization_params.py`."
        ),
    )
    extra_group.add_argument(
        "--skip-extra-artifacts",
        dest="copy_extra_artifacts",
        action="store_false",
        help="Do not copy extra top-level artifacts from the legacy dataset.",
    )
    parser.set_defaults(copy_extra_artifacts=True)
    parser.add_argument(
        "--hf-cache-root",
        type=str,
        default=None,
        help=(
            "Optional root directory for temporary HF cache env vars used by the "
            "official LeRobot converter."
        ),
    )
    return parser


def resolve_dataset_arg(
    parser: argparse.ArgumentParser,
    *,
    dataset: str | None,
    dataset_option: str | None,
) -> str:
    if dataset is not None and dataset_option is not None:
        parser.error("Specify the dataset either positionally or via --dataset, not both.")
    resolved = dataset_option if dataset_option is not None else dataset
    if resolved is None:
        parser.error("the following arguments are required: dataset")
    return resolved


def resolve_output_dir(source_root: Path, output_dir: str | None) -> Path:
    if output_dir is None:
        source_name = source_root.name
        if source_name.endswith("_v30"):
            source_name = f"{source_name}_converted"
        return (source_root.parent / f"{source_name}_v30").resolve()

    raw = Path(output_dir).expanduser()
    if raw.is_absolute():
        return raw.resolve()

    output_text = raw.as_posix()
    if output_text.startswith("data/"):
        return (MAIN_ROOT / raw).resolve()
    return (MAIN_ROOT / "data" / raw).resolve()


def validate_conversion_paths(
    *,
    source_root: Path,
    output_root: Path,
    overwrite_output: bool,
) -> None:
    if output_root == source_root:
        raise ValueError(
            "Refusing to convert in place. Please use --output-dir to write the "
            "v3.0 dataset into a new directory."
        )
    if source_root in output_root.parents:
        raise ValueError(
            "Output directory cannot be created inside the source dataset directory. "
            f"source={source_root}, output={output_root}"
        )
    if output_root in source_root.parents:
        raise ValueError(
            "Output directory cannot be a parent of the source dataset directory. "
            f"source={source_root}, output={output_root}"
        )
    if output_root.exists() and not overwrite_output:
        raise FileExistsError(
            f"Output directory already exists: {output_root}. "
            "Pass --overwrite-output to replace it."
        )


def scan_dataset(dataset_root: Path) -> DatasetScanSummary:
    tqdm = get_tqdm()
    progress = tqdm(desc="Scanning input", unit="file") if tqdm is not None else None
    total_files = 0
    data_parquet_files = 0
    video_files = 0
    extra_files = 0
    extra_entries: set[str] = set()
    try:
        for path in dataset_root.rglob("*"):
            if not path.is_file():
                continue
            total_files += 1
            rel = path.relative_to(dataset_root)
            top_level = rel.parts[0] if rel.parts else path.name
            suffix = path.suffix.lower()

            if top_level == "data" and suffix == ".parquet":
                data_parquet_files += 1
            elif top_level == "videos" and suffix in {".mp4", ".avi", ".mov", ".mkv"}:
                video_files += 1
            elif top_level not in KNOWN_V30_ROOT_ENTRIES:
                extra_files += 1
                extra_entries.add(top_level)

            if progress is not None:
                progress.update(1)
    finally:
        if progress is not None:
            progress.close()

    return DatasetScanSummary(
        total_files=total_files,
        data_parquet_files=data_parquet_files,
        video_files=video_files,
        extra_files=extra_files,
        extra_entries=sorted(extra_entries),
    )


def list_extra_artifact_files(source_root: Path) -> list[Path]:
    files: list[Path] = []
    for child in sorted(source_root.iterdir()):
        if child.name in KNOWN_V30_ROOT_ENTRIES:
            continue
        if child.name == "__pycache__":
            continue
        if child.is_file():
            files.append(child.relative_to(source_root))
            continue
        if child.is_dir():
            for nested in sorted(child.rglob("*")):
                if nested.is_file():
                    files.append(nested.relative_to(source_root))
    return files


def copy_extra_artifacts(
    *,
    source_root: Path,
    output_root: Path,
    progress=None,
) -> int:
    extra_files = list_extra_artifact_files(source_root)
    if not extra_files:
        return 0

    tqdm = get_tqdm()
    file_progress = (
        tqdm(total=len(extra_files), desc="Copying extra artifacts", unit="file")
        if tqdm is not None
        else None
    )
    copied = 0
    try:
        for rel_path in extra_files:
            src = source_root / rel_path
            dst = output_root / rel_path
            dst.parent.mkdir(parents=True, exist_ok=True)
            shutil.copy2(src, dst)
            copied += 1
            if file_progress is not None:
                file_progress.update(1)
    finally:
        if file_progress is not None:
            file_progress.close()

    progress_write(
        progress,
        f"[info] copied {copied} extra artifact files from source dataset.",
    )
    return copied


def import_lerobot_converter():
    try:
        from lerobot.datasets.utils import (
            DEFAULT_DATA_FILE_SIZE_IN_MB,
            DEFAULT_VIDEO_FILE_SIZE_IN_MB,
        )
        from lerobot.datasets.v30.convert_dataset_v21_to_v30 import (
            convert_data,
            convert_episodes_metadata,
            convert_info,
            convert_tasks,
            convert_videos,
            validate_local_dataset_version,
        )
    except ModuleNotFoundError as exc:
        raise RuntimeError(
            "Missing `lerobot` conversion dependencies. Activate the intended env "
            "first, for example `conda activate corl-py312`."
        ) from exc

    return {
        "DEFAULT_DATA_FILE_SIZE_IN_MB": DEFAULT_DATA_FILE_SIZE_IN_MB,
        "DEFAULT_VIDEO_FILE_SIZE_IN_MB": DEFAULT_VIDEO_FILE_SIZE_IN_MB,
        "convert_data": convert_data,
        "convert_episodes_metadata": convert_episodes_metadata,
        "convert_info": convert_info,
        "convert_tasks": convert_tasks,
        "convert_videos": convert_videos,
        "validate_local_dataset_version": validate_local_dataset_version,
    }


@contextlib.contextmanager
def temporary_hf_cache_env(cache_root: Path):
    cache_root = cache_root.resolve()
    hf_home = cache_root / ".hf_home"
    hf_datasets_cache = cache_root / ".hf_datasets"
    xdg_cache_home = cache_root / ".xdg_cache"
    hf_home.mkdir(parents=True, exist_ok=True)
    hf_datasets_cache.mkdir(parents=True, exist_ok=True)
    xdg_cache_home.mkdir(parents=True, exist_ok=True)

    previous_hf_home = os.environ.get("HF_HOME")
    previous_hf_datasets_cache = os.environ.get("HF_DATASETS_CACHE")
    previous_xdg_cache_home = os.environ.get("XDG_CACHE_HOME")

    try:
        os.environ["HF_HOME"] = str(hf_home)
        os.environ["HF_DATASETS_CACHE"] = str(hf_datasets_cache)
        os.environ["XDG_CACHE_HOME"] = str(xdg_cache_home)
        yield
    finally:
        if previous_hf_home is None:
            os.environ.pop("HF_HOME", None)
        else:
            os.environ["HF_HOME"] = previous_hf_home
        if previous_hf_datasets_cache is None:
            os.environ.pop("HF_DATASETS_CACHE", None)
        else:
            os.environ["HF_DATASETS_CACHE"] = previous_hf_datasets_cache
        if previous_xdg_cache_home is None:
            os.environ.pop("XDG_CACHE_HOME", None)
        else:
            os.environ["XDG_CACHE_HOME"] = previous_xdg_cache_home


def run_stage(stage_progress, name: str, fn, *args, **kwargs):
    progress_write(stage_progress, f"[stage] {name}")
    if stage_progress is not None:
        stage_progress.set_description(f"Convert: {name}")
    result = fn(*args, **kwargs)
    if stage_progress is not None:
        stage_progress.update(1)
    return result


def write_conversion_metadata(
    *,
    output_root: Path,
    source_root: Path,
    copied_extra_files: int,
) -> None:
    metadata = {
        "converted_from": str(source_root.resolve()),
        "converted_at": dt.datetime.now(dt.timezone.utc).isoformat(),
        "tool": str(Path(__file__).resolve()),
        "copied_extra_files": int(copied_extra_files),
    }
    (output_root / DEFAULT_CONVERSION_METADATA_FILENAME).write_text(
        json.dumps(metadata, indent=2, ensure_ascii=False),
        encoding="utf-8",
    )


def main() -> int:
    parser = build_parser()
    args = parser.parse_args()
    dataset_arg = resolve_dataset_arg(
        parser,
        dataset=args.dataset,
        dataset_option=args.dataset_option,
    )

    source_root = resolve_dataset_root(dataset_arg)
    if is_lerobot_dataset_root(source_root):
        raise ValueError(
            f"Dataset is already LeRobot v3.0 and does not need conversion: {source_root}"
        )
    if not is_legacy_lerobot_dataset_root(source_root):
        raise ValueError(
            "Resolved dataset is not a recognized legacy LeRobot v2.1 dataset: "
            f"{source_root}"
        )

    output_root = resolve_output_dir(source_root, args.output_dir)
    validate_conversion_paths(
        source_root=source_root,
        output_root=output_root,
        overwrite_output=bool(args.overwrite_output),
    )

    scan_summary = scan_dataset(source_root)
    print("[info] source dataset")
    print(f"  root: {source_root}")
    print(f"  total_files: {scan_summary.total_files}")
    print(f"  data_parquet_files: {scan_summary.data_parquet_files}")
    print(f"  video_files: {scan_summary.video_files}")
    print(f"  extra_files: {scan_summary.extra_files}")
    if scan_summary.extra_entries:
        print(f"  extra_entries: {', '.join(scan_summary.extra_entries)}")
    print(f"[info] output dataset: {output_root}")

    if output_root.exists():
        shutil.rmtree(output_root)
    output_root.parent.mkdir(parents=True, exist_ok=True)
    temp_root = output_root.parent / f".{output_root.name}.tmp"
    if temp_root.exists():
        shutil.rmtree(temp_root)
    temp_root.mkdir(parents=True, exist_ok=False)

    converter = import_lerobot_converter()

    hf_cache_root = (
        Path(args.hf_cache_root).expanduser().resolve()
        if args.hf_cache_root is not None
        else DEFAULT_LEROBOT_V30_COMPAT_CACHE_ROOT
    )

    stage_count = 7 + (1 if args.copy_extra_artifacts else 0)
    tqdm = get_tqdm()
    stage_progress = (
        tqdm(total=stage_count, desc="Convert: setup", unit="stage")
        if tqdm is not None
        else None
    )

    copied_extra_files = 0
    try:
        with temporary_hf_cache_env(hf_cache_root):
            run_stage(
                stage_progress,
                "validate legacy dataset",
                converter["validate_local_dataset_version"],
                source_root,
            )
            run_stage(
                stage_progress,
                "convert info",
                converter["convert_info"],
                source_root,
                temp_root,
                converter["DEFAULT_DATA_FILE_SIZE_IN_MB"],
                converter["DEFAULT_VIDEO_FILE_SIZE_IN_MB"],
            )
            run_stage(
                stage_progress,
                "convert tasks",
                converter["convert_tasks"],
                source_root,
                temp_root,
            )
            episodes_metadata = run_stage(
                stage_progress,
                "convert tabular data",
                converter["convert_data"],
                source_root,
                temp_root,
                converter["DEFAULT_DATA_FILE_SIZE_IN_MB"],
            )
            episodes_videos_metadata = run_stage(
                stage_progress,
                "convert videos",
                converter["convert_videos"],
                source_root,
                temp_root,
                converter["DEFAULT_VIDEO_FILE_SIZE_IN_MB"],
            )
            run_stage(
                stage_progress,
                "convert episode metadata",
                converter["convert_episodes_metadata"],
                source_root,
                temp_root,
                episodes_metadata,
                episodes_videos_metadata,
            )
            if args.copy_extra_artifacts:
                copied_extra_files = run_stage(
                    stage_progress,
                    "copy extra artifacts",
                    copy_extra_artifacts,
                    source_root=source_root,
                    output_root=temp_root,
                    progress=stage_progress,
                )
            run_stage(
                stage_progress,
                "write conversion metadata",
                write_conversion_metadata,
                output_root=temp_root,
                source_root=source_root,
                copied_extra_files=copied_extra_files,
            )

        shutil.move(str(temp_root), str(output_root))
    except Exception:
        if temp_root.exists():
            shutil.rmtree(temp_root, ignore_errors=True)
        raise
    finally:
        if stage_progress is not None:
            stage_progress.close()

    print("[done] conversion completed")
    print(f"  source: {source_root}")
    print(f"  output: {output_root}")
    print(f"  copied_extra_files: {copied_extra_files}")
    print(
        "  verify: "
        f"meta/info.json={(output_root / 'meta/info.json').exists()}, "
        f"meta/stats.json={(output_root / 'meta/stats.json').exists()}, "
        f"meta/episodes/chunk-000/file-000.parquet={((output_root / 'meta/episodes/chunk-000/file-000.parquet').exists())}"
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
